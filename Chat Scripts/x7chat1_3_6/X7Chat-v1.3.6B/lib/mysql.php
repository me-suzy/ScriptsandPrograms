function DoQuery($query){
$q = @mysql_query($query);
if(mysql_error() == ""){
return $q;
}else{
return mysql_error();
}
}

function Do_Fetch_Row($q){
$row = @mysql_fetch_row($q);
return $row;
}

function DoConnect($HOST,$USER,$PASS){
$con = @mysql_connect($HOST,$USER,$PASS);
return $con;
}

function DoSelectDb($DB){
$dbcon = @mysql_select_db($DB);
return $dbcon;
}

